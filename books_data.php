// ── GÉNÉRATEUR D'EXTRAITS ────────────────────────────────────
function _extrait(string $titre, string $auteur, string $synopsis, array $chapitres): string {
    $pages = [];
    $descriptions = [
        "Les premières pages de cette œuvre posent les fondements d'un univers dense et cohérent. {$auteur} maîtrise l'art de l'incipit : en quelques lignes, le ton est donné, les enjeux esquissés.\n\nLe lecteur est immédiatement saisi par la qualité de la prose, la précision des descriptions et la profondeur des personnages. Rien n'est superflu, chaque mot porte son poids de sens.\n\n{$synopsis}",
        "La narration s'approfondit et révèle ses couches successives. {$auteur} multiplie les points de vue, les registres et les niveaux de lecture.\n\nCe qui semblait simple en surface révèle des abysses de complexité. Les thèmes se répondent, s'amplifient, créant une harmonie narrative d'une rare sophistication.\n\nLe lecteur avance dans l'œuvre comme on entre dans une cathédrale : découvrant à chaque pas de nouveaux détails, de nouvelles perspectives.",
        "Le cœur battant de l'œuvre. {$auteur} réunit ici toutes les tensions dramatiques accumulées depuis le début.\n\nLes contradictions des personnages atteignent leur paroxysme. Les choix impossibles doivent être faits. La vérité ne peut plus être évitée.\n\nC'est la grandeur des grandes œuvres : elles nous placent face à des questions auxquelles il n'existe pas de réponse simple.",
        "La résolution, attendue et pourtant surprenante. {$auteur} refuse les facilités du happy-end artificiel comme du désespoir systématique.\n\nIl y a dans cette conclusion quelque chose de juste, d'inévitable, comme si toute l'œuvre n'avait fait que préparer cet instant de grâce ou de fracas.\n\nOn referme le livre avec l'impression d'avoir vécu quelque chose d'important.",
        "Après la lecture de \"{$titre}\", on comprend pourquoi {$auteur} occupe une place si importante dans la littérature mondiale.\n\nThis œuvre survive because it speaks directly to the deepest part of our humanity.\n\nRevenez-y dans quelques années. Vous serez différent, l'œuvre aussi vous semblera différente — et c'est précisément pour cela qu'on appelle certains livres des chefs-d'œuvre.",
    ];
    foreach ($chapitres as $i => $chap) {
        $desc = $descriptions[$i % count($descriptions)];
        $pages[] = "CHAPITRE " . ($i + 1) . " — {$chap}\n\n{$desc}";
    }
    return implode('||||PAGE||||', $pages);
}